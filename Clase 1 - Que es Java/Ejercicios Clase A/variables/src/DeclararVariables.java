/*Es hora de que crees tus primeras variables en java
  La idea es que crees tres variables: una que se va a llamar numeroEntero
  y va a ser de tipo int, una que se va a llamar numeroConComa de tipo double
  y por último una llamada nombre de tipo string

  Luego de declararlas asignarles un valor acorde a su tipo*/

public class DeclararVariables {
    public static void main(String[] args) {
        int numeroEntero = 10;
        double numeroConComa = 20.51;
        String nombre = "Montserrat";

        System.out.println("El número entero es: " + numeroEntero);
        System.out.println("El número con coma es: " + numeroConComa);
        System.out.println("El nombre es: " + nombre);
    }
}
